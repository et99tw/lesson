void main(){
  //弱類型
  //聲明變量 不初始值
  var a;
  a = 'EricOreo';
  a = 123;
  a= false;
  print(a);

  //聲明變量 給初始值
  var b = 'EricOreo';
  print(b);

  //dynamic
  dynamic c = 'EricOreo';
  c = 123;
  c = false;

  //object
  

  //強類型
  String aa = 'EricOreo';
  bool bb = false;

  //一切對象的默認值是NULL
  bool bbb;
  String aaa;
  print(bbb);
  print(aaa);

}